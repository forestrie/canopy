/**
 * Export TS-signed on-chain delegation proof vectors for arbor/univocity
 * contract acceptance (FOR-332 / plan-0050 R3).
 *
 * Deterministic keys so the JSON is stable across regenerations.
 *
 * Usage (from package root):
 *   pnpm build && node scripts/export-onchain-vectors.mjs
 */

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { p256 } from "@noble/curves/p256";
import { keccak_256 } from "@noble/hashes/sha3";
import { sha256 } from "@noble/hashes/sha256";
import { secp256k1 } from "@noble/curves/secp256k1";
import {
  buildOnchainDelegationToBeSignedEs256,
  buildOnchainDelegationToBeSignedKs256,
  normalizeEs256SignatureLowS,
  signOnchainDelegationKs256,
} from "../dist/index.js";

function bytesToHex(bytes) {
  return Array.from(bytes)
    .map((b) => b.toString(16).padStart(2, "0"))
    .join("");
}

const FIXTURE_LOG_ID_HEX = "101112131415161718191a1b1c1d1e1f";
const FIXTURE_MMR_START = 3n;
const FIXTURE_MMR_END = 1n << 40n;
const FIXTURE_X = new Uint8Array(32).map((_, i) => 0xa0 + i);
const FIXTURE_Y = new Uint8Array(32).map((_, i) => 0xc0 + i);
// Anvil account 0 — same KS256 root used by arbor publishproof tests.
const KS256_PRIVATE_KEY_HEX =
  "ac0974bec39a17e36ba4a6b4d238ff944bacb478cbed5efcae784d7bf4f2ff80";
// Fixed P-256 scalar for a stable ES256 vector (not a production key).
const ES256_PRIVATE_KEY_HEX =
  "c1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1f1";

const input = {
  logIdHex: FIXTURE_LOG_ID_HEX,
  mmrStart: FIXTURE_MMR_START,
  mmrEnd: FIXTURE_MMR_END,
  delegatedKeyX: FIXTURE_X,
  delegatedKeyY: FIXTURE_Y,
};

const ks = signOnchainDelegationKs256(input, KS256_PRIVATE_KEY_HEX);
const ksPub = secp256k1.getPublicKey(KS256_PRIVATE_KEY_HEX, false);
const address = keccak_256(ksPub.slice(1)).slice(-20);

const esTbs = buildOnchainDelegationToBeSignedEs256(input);
const esDigest = sha256(esTbs.sigStructureBytes);
const esSig = p256.sign(esDigest, ES256_PRIVATE_KEY_HEX);
const esRaw = normalizeEs256SignatureLowS(esSig.toCompactRawBytes());
const esPub = p256.getPublicKey(ES256_PRIVATE_KEY_HEX, false);
const rootX = esPub.slice(1, 33);
const rootY = esPub.slice(33, 65);

const vectors = {
  description:
    "TS-signed on-chain delegation proofs (FOR-332 / plan-0050 R3). " +
    "Generated by @forestrie/delegation-cose scripts/export-onchain-vectors.mjs.",
  ks256: {
    alg: "KS256",
    logIdHex: FIXTURE_LOG_ID_HEX,
    mmrStart: FIXTURE_MMR_START.toString(),
    mmrEnd: FIXTURE_MMR_END.toString(),
    delegatedKeyX: bytesToHex(FIXTURE_X),
    delegatedKeyY: bytesToHex(FIXTURE_Y),
    rootAddress: bytesToHex(address),
    protectedHeader: bytesToHex(ks.protectedHeader),
    delegationKey: bytesToHex(ks.delegationKey),
    signature: bytesToHex(ks.signature),
    sigStructure: bytesToHex(
      buildOnchainDelegationToBeSignedKs256(input).sigStructureBytes,
    ),
  },
  es256: {
    alg: "ES256",
    logIdHex: FIXTURE_LOG_ID_HEX,
    mmrStart: FIXTURE_MMR_START.toString(),
    mmrEnd: FIXTURE_MMR_END.toString(),
    delegatedKeyX: bytesToHex(FIXTURE_X),
    delegatedKeyY: bytesToHex(FIXTURE_Y),
    rootX: bytesToHex(rootX),
    rootY: bytesToHex(rootY),
    protectedHeader: bytesToHex(esTbs.protectedHeader),
    delegationKey: bytesToHex(esTbs.delegationKey),
    signature: bytesToHex(esRaw),
    sigStructure: bytesToHex(esTbs.sigStructureBytes),
  },
};

const pkgRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const outDir = join(pkgRoot, "testdata");
mkdirSync(outDir, { recursive: true });
const outPath = join(outDir, "onchain-delegation-vectors.json");
writeFileSync(outPath, JSON.stringify(vectors, null, 2) + "\n");
console.log(`wrote ${outPath}`);
